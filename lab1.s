		AREA LAB1, CODE, READONLY
		ARM
AD_RAM	EQU	0XE0001000
RAM	RN	R11	
			
	ENTRY
	

			LDR RAM,=AD_RAM
			LDR	R0, =0X00000051		;BASE
			LDR R1,	=0X00000002		;POTENCIA
			LDR R2,	=0X00000006  	;COD. OP

			CMP R2, #0X00000001
			BEQ SUMA

			CMP R2, #0X00000002
			BEQ RESTA 
			
			CMP R2, #0X00000003
			BEQ DIVISION
			
			CMP R2, #0X00000004
			BEQ MULTIPLICACION 
			
			CMP R2, #0X00000005
			BEQ POTENCIACION
			
			CMP R2, #0X00000006
			BEQ RADICACION
			
			CMP R2, #0X00000007
			BEQ FACTORIAL

FIN2
			MOV R3,R0
			STR R3, [RAM]
			
FIN			
			STR R3, [RAM]
			
HERE		B	HERE



SUMA
			ADD R3, R0 ,R1
			B FIN
			
RESTA		
			SUB R3, R0, R1
			B FIN


MULTIPLICACION
			MUL	R3, R0,R1
			B FIN

DIVISION 
			LDR R3,	=0X0	;CONTADOR
			CMP R1,#0X0
			BNE DIV
			B FIN
			

DIV
			CMP R0,R1
			BLO FIN
			SUB	R0, R0, R1
			ADD R3, R3, #0X1
			B 	DIV
			


POTENCIACION
			LDR R5,	=0X00000001		;CONTADOR
			MOV R4,R0


POTEN			CMP R5,R1
			BEQ FIN
			MUL R3,R4,R0
			MOV R4,R3
			ADD R5,R5,#0X00000001
			B POTEN
			
			
RADICACION
			LDR R1, =0X0
			B RAD
RAD
			MOV R3, R1
			MUL R5, R1, R1
			ADD R1, #0X1
			CMP R0, R5
			BEQ	FIN 
			CMP R1, R0
			BGT	FIN2
			B RAD

FACTORIAL	
			CMP R0,#0X1
			BEQ FIN2
			CMP R0,#0X0
			BEQ FIN2
			LDR	R1,	=0X00000001
			SUB R4, R0, R1
			B FACTO
			

FACTO		MUL	R3, R0,R4
			MOV R0, R3
			SUB R4, R4, R1
			CMP R4, #0X1
			BNE FACTO
			B	FIN

	END